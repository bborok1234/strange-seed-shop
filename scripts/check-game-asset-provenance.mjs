import fs from "node:fs";

const manifestPath = "public/assets/manifest/assetManifest.json";
const promptsPath = "assets/source/asset_prompts.json";
const statusPath = "assets/source/asset_generation_status.json";
const failures = [];

function readJson(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch (error) {
    failures.push(`${filePath} must parse as JSON: ${error.message}`);
    return null;
  }
}

function toLocalPath(assetPath = "") {
  return assetPath.startsWith("/") ? `public${assetPath}` : assetPath;
}

function readPngSize(filePath) {
  const buffer = fs.readFileSync(filePath);
  if (buffer.length < 24 || buffer.toString("ascii", 1, 4) !== "PNG") {
    return null;
  }

  return {
    width: buffer.readUInt32BE(16),
    height: buffer.readUInt32BE(20)
  };
}

const manifest = readJson(manifestPath);
const prompts = readJson(promptsPath);
const status = readJson(statusPath);
const promptById = new Map((prompts?.prompts ?? []).map((prompt) => [prompt.asset_id, prompt]));
const statusOutputs = new Set(status?.outputs ?? []);
const allowedGenerationModes = new Set(["codex_native_image_generation", "gpt-image-2"]);
const gameGraphicCategories = new Set(["creature", "seed_icon", "ui_frame", "sprite_strip", "fx_strip", "shop_image", "background"]);

if (!allowedGenerationModes.has(prompts?.generation_mode)) {
  failures.push(`${promptsPath} generation_mode must be codex_native_image_generation or gpt-image-2`);
}

if (!allowedGenerationModes.has(status?.generation_mode)) {
  failures.push(`${statusPath} generation_mode must be codex_native_image_generation or gpt-image-2`);
}

for (const phrase of status?.notes ?? []) {
  if (/repo-native vector|svg/i.test(phrase) && !/reject/i.test(phrase)) {
    failures.push(`${statusPath} notes must not accept repo-native vector/SVG game assets: ${phrase}`);
  }
}

let acceptedGameAssets = 0;
let checkedAnimatedAssets = 0;

for (const [assetId, asset] of Object.entries(manifest?.assets ?? {})) {
  if (asset.status !== "accepted") continue;
  if (!gameGraphicCategories.has(asset.category)) continue;

  acceptedGameAssets += 1;
  const localPath = toLocalPath(asset.path);
  const prompt = promptById.get(assetId);

  if (!prompt) {
    failures.push(`${assetId} accepted game asset is missing an entry in ${promptsPath}`);
  } else if (prompt.output_path !== localPath) {
    failures.push(`${assetId} prompt output_path ${prompt.output_path} does not match manifest path ${localPath}`);
  }

  if (!statusOutputs.has(localPath)) {
    failures.push(`${assetId} accepted game asset path missing from ${statusPath} outputs: ${localPath}`);
  }

  if (!/\.png$/i.test(localPath)) {
    failures.push(`${assetId} accepted game graphics must be PNG generated by Codex image generation or gpt-image-2, got ${localPath}`);
    continue;
  }

  if (!fs.existsSync(localPath)) {
    failures.push(`${assetId} missing local file at ${localPath}`);
    continue;
  }

  const size = readPngSize(localPath);
  if (!size) {
    failures.push(`${assetId} must be a readable PNG file`);
    continue;
  }

  if (asset.width !== size.width || asset.height !== size.height) {
    failures.push(`${assetId} manifest dimensions ${asset.width}x${asset.height} do not match PNG ${size.width}x${size.height}`);
  }

  if (asset.category === "sprite_strip" || asset.category === "fx_strip") {
    checkedAnimatedAssets += 1;
    const animation = asset.animation;
    if (!animation || animation.kind !== "spritesheet") {
      failures.push(`${assetId} ${asset.category} must include spritesheet animation metadata`);
      continue;
    }

    if (!animation.binding) {
      failures.push(`${assetId} ${asset.category} must include animation.binding so runtime use is explicit`);
    }

    const margin = animation.margin ?? 0;
    const spacing = animation.spacing ?? 0;
    const expectedWidth = margin * 2 + animation.frameWidth * animation.frames + spacing * (animation.frames - 1);
    const expectedHeight = margin * 2 + animation.frameHeight;
    if (size.width !== expectedWidth || size.height !== expectedHeight) {
      failures.push(`${assetId} spritesheet slicing mismatch: expected ${expectedWidth}x${expectedHeight}, got ${size.width}x${size.height}`);
    }
  }
}

for (const prompt of prompts?.prompts ?? []) {
  if (/^public\/assets\/game\//.test(prompt.output_path) && !/\.png$/i.test(prompt.output_path)) {
    failures.push(`${prompt.asset_id} prompt output_path must be PNG for game graphics: ${prompt.output_path}`);
  }
}

if (acceptedGameAssets === 0) {
  failures.push("expected at least one accepted game asset");
}

console.log(JSON.stringify({ ok: failures.length === 0, acceptedGameAssets, checkedAnimatedAssets, failures }, null, 2));

if (failures.length > 0) {
  process.exit(1);
}
